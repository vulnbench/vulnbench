"""VulnBench evaluation harness CLI.

Uses an LLM judge (Claude Opus 4.6 via OpenRouter) to evaluate whether
model-generated patches correctly fix the vulnerability described in each
benchmark instance.

Usage:
    # With a LiteLLM model (requires appropriate API key in env)
    python -m benchmark.run_eval \
        --benchmark data/benchmark/vulnbench_mini.json \
        --model openrouter/openai/gpt-5.4 \
        --limit 5

    # With a custom adapter module
    python -m benchmark.run_eval \
        --benchmark data/benchmark/vulnbench_mini.json \
        --adapter benchmark.adapters.base \
        --output results/eval_report.json
"""

from __future__ import annotations

from dotenv import load_dotenv
load_dotenv()

import argparse
import importlib
import json
import logging
import re
import time
from datetime import datetime, timezone
from pathlib import Path

import litellm
from tqdm import tqdm

from benchmark.eval_models import (
    AggregateMetrics,
    EvalReport,
    InstanceResult,
    PatchAnalysis,
)
from src.benchmark_models import BenchmarkDatabase, BenchmarkInstance

logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s [%(levelname)s] %(name)s: %(message)s",
)
logger = logging.getLogger(__name__)

JUDGE_MODEL = "openrouter/anthropic/claude-opus-4-6"

JUDGE_SYSTEM_PROMPT = """\
You are a security patch reviewer. You evaluate whether a proposed patch \
correctly fixes a specific CVE vulnerability.

You will be given:
1. A vulnerability description (CVE ID, CWE, affected files, etc.)
2. The gold (reference) patch that is known to fix the vulnerability
3. A candidate patch generated by an AI model

Your job is to judge whether the candidate patch effectively fixes the \
vulnerability. The candidate does NOT need to be identical to the gold patch — \
different implementations can be equally valid. Focus on whether the candidate:

- Addresses the root cause of the vulnerability
- Does not introduce new vulnerabilities or break existing functionality
- Covers the same scope as the gold patch (doesn't miss affected files/paths)

Respond with ONLY a JSON object (no markdown fencing, no extra text):
{
  "score": <float 0.0 to 1.0>,
  "verdict": "<pass or fail>",
  "reasoning": "<1-3 sentences explaining your judgment>"
}

Scoring guide:
- 1.0: Fully correct fix, addresses the vulnerability completely
- 0.7-0.9: Mostly correct, minor issues but would effectively mitigate the vuln
- 0.5-0.6: Partially correct, addresses some aspects but has gaps
- 0.2-0.4: Shows understanding of the issue but fix is insufficient
- 0.0-0.1: Wrong, empty, or irrelevant patch
"""


def render_prompt(instance: BenchmarkInstance) -> str:
    """Render a BenchmarkInstance's TaskPrompt into a single flat string."""
    tp = instance.task_prompt
    parts = [tp.system_context, "", tp.vulnerability_description]

    if tp.cwe_category:
        parts.append(f"\nCWE Category: {tp.cwe_category}")
    if tp.cwe_guidance:
        parts.append(f"Guidance: {tp.cwe_guidance}")
    if tp.affected_files_hint:
        parts.append(f"\nAffected files: {', '.join(tp.affected_files_hint)}")

    parts.append(f"\n{tp.instructions}")
    return "\n".join(parts)


def render_prompt_parts(instance: BenchmarkInstance) -> tuple[str, str]:
    """Render a BenchmarkInstance's TaskPrompt into (system_msg, user_msg)."""
    tp = instance.task_prompt

    system_msg = tp.system_context

    user_parts = [tp.vulnerability_description]
    if tp.cwe_category:
        user_parts.append(f"\nCWE Category: {tp.cwe_category}")
    if tp.cwe_guidance:
        user_parts.append(f"Guidance: {tp.cwe_guidance}")
    if tp.affected_files_hint:
        user_parts.append(f"\nAffected files: {', '.join(tp.affected_files_hint)}")
    user_parts.append(f"\n{tp.instructions}")

    user_msg = "\n".join(user_parts)

    return system_msg, user_msg


def parse_diff_from_output(output: str) -> str:
    """Extract a unified diff from model output."""
    # Try fenced code block first
    fence_match = re.search(
        r"```(?:diff|patch)?\s*\n(.*?)```",
        output,
        re.DOTALL,
    )
    if fence_match:
        return fence_match.group(1).strip()

    # Try raw diff
    diff_match = re.search(
        r"(diff --git.*)",
        output,
        re.DOTALL,
    )
    if diff_match:
        return diff_match.group(1).strip()

    # Try --- / +++ block
    patch_match = re.search(
        r"(---\s+\S+.*?\n\+\+\+\s+\S+.*?\n@@.*)",
        output,
        re.DOTALL,
    )
    if patch_match:
        return patch_match.group(1).strip()

    return output.strip()


def judge_patch(
    instance: BenchmarkInstance,
    model_patch: str,
    judge_model: str = JUDGE_MODEL,
) -> PatchAnalysis:
    """Use an LLM judge to evaluate the candidate patch against the gold patch."""
    if not model_patch.strip():
        return PatchAnalysis(
            judge_score=0.0,
            judge_reasoning="Empty or no patch produced.",
            judge_verdict="fail",
        )

    gold_diff = instance.gold_patch.raw_diff
    tp = instance.task_prompt

    judge_user_prompt = f"""\
## Vulnerability
- CVE: {instance.cve_id}
- CWE: {', '.join(instance.cwe_ids)}
- Ecosystem: {instance.ecosystem}
- Package: {instance.package_name}
- Severity: {instance.severity}
- Description: {tp.vulnerability_description}
- Affected files: {', '.join(tp.affected_files_hint) if tp.affected_files_hint else 'Not specified'}

## Gold (Reference) Patch
```diff
{gold_diff}
```

## Candidate Patch
```diff
{model_patch}
```

Judge whether the candidate patch correctly fixes the vulnerability."""

    try:
        response = litellm.completion(
            model=judge_model,
            messages=[
                {"role": "system", "content": JUDGE_SYSTEM_PROMPT},
                {"role": "user", "content": judge_user_prompt},
            ],
            temperature=0.0,
            max_tokens=512,
            num_retries=2,
        )

        raw = response.choices[0].message.content or ""

        # Parse JSON from response, handling potential markdown fencing
        json_text = raw.strip()
        json_match = re.search(r"\{.*\}", json_text, re.DOTALL)
        if json_match:
            json_text = json_match.group(0)

        result = json.loads(json_text)

        score = float(result.get("score", 0.0))
        score = max(0.0, min(1.0, score))
        verdict = result.get("verdict", "fail").lower()
        reasoning = result.get("reasoning", "")

        return PatchAnalysis(
            judge_score=round(score, 4),
            judge_reasoning=reasoning,
            judge_verdict=verdict,
        )

    except Exception as e:
        logger.warning("Judge failed for %s: %s", instance.instance_id, e)
        return PatchAnalysis(
            judge_score=0.0,
            judge_reasoning=f"Judge error: {e}",
            judge_verdict="fail",
        )


def _is_litellm_adapter(adapter) -> bool:
    """Check if adapter is a LiteLLMAdapter."""
    try:
        from benchmark.adapters.litellm_adapter import LiteLLMAdapter
        return isinstance(adapter, LiteLLMAdapter)
    except ImportError:
        return False


def evaluate_instance(
    instance: BenchmarkInstance,
    adapter,
    judge_model: str = JUDGE_MODEL,
) -> InstanceResult:
    """Evaluate a single benchmark instance."""
    is_litellm = _is_litellm_adapter(adapter)

    # Generate patch
    start = time.monotonic()
    try:
        if is_litellm:
            system_msg, user_msg = render_prompt_parts(instance)
            raw_output = adapter.generate_patch(user_msg, system_prompt=system_msg)
        else:
            prompt = render_prompt(instance)
            raw_output = adapter.generate_patch(prompt)
    except Exception as e:
        logger.warning("Adapter failed for %s: %s", instance.instance_id, e)
        raw_output = ""
    gen_time = time.monotonic() - start

    # Collect token/cost metadata
    prompt_tokens = 0
    completion_tokens = 0
    cost_usd = 0.0
    if is_litellm:
        meta = adapter.last_response_meta
        prompt_tokens = meta.get("prompt_tokens", 0)
        completion_tokens = meta.get("completion_tokens", 0)
        cost_usd = meta.get("cost_usd", 0.0)

    # Parse diff from output
    model_patch = parse_diff_from_output(raw_output)

    # Judge the patch
    analysis = judge_patch(instance, model_patch, judge_model=judge_model)
    score = analysis.judge_score
    passed = analysis.judge_verdict == "pass" or score >= 0.5

    return InstanceResult(
        instance_id=instance.instance_id,
        cve_id=instance.cve_id,
        difficulty_tier=instance.difficulty_tier.value,
        ecosystem=instance.ecosystem,
        model_patch=model_patch,
        generation_time_s=round(gen_time, 3),
        patch_analysis=analysis,
        score=score,
        passed=passed,
        prompt_tokens=prompt_tokens,
        completion_tokens=completion_tokens,
        cost_usd=cost_usd,
    )


def compute_aggregate(results: list[InstanceResult]) -> AggregateMetrics:
    """Compute aggregate metrics from individual results."""
    if not results:
        return AggregateMetrics()

    total = len(results)
    passed = sum(1 for r in results if r.passed)
    mean_score = sum(r.score for r in results) / total
    mean_time = sum(r.generation_time_s for r in results) / total

    # Per-tier
    tier_passed: dict[str, list[bool]] = {}
    for r in results:
        tier_passed.setdefault(r.difficulty_tier, []).append(r.passed)
    tier_rates = {t: sum(v) / len(v) for t, v in tier_passed.items() if v}

    # Per-ecosystem
    eco_passed: dict[str, list[bool]] = {}
    for r in results:
        eco_passed.setdefault(r.ecosystem, []).append(r.passed)
    eco_rates = {e: sum(v) / len(v) for e, v in eco_passed.items() if v}

    # Cost totals
    total_cost = sum(r.cost_usd for r in results)
    total_judge_cost = sum(r.judge_cost_usd for r in results)
    total_prompt = sum(r.prompt_tokens for r in results)
    total_completion = sum(r.completion_tokens for r in results)

    return AggregateMetrics(
        total_instances=total,
        total_passed=passed,
        pass_rate=round(passed / total, 4),
        mean_score=round(mean_score, 4),
        pass_rate_by_tier={k: round(v, 4) for k, v in tier_rates.items()},
        pass_rate_by_ecosystem={k: round(v, 4) for k, v in eco_rates.items()},
        mean_generation_time_s=round(mean_time, 3),
        total_cost_usd=round(total_cost, 6),
        total_judge_cost_usd=round(total_judge_cost, 6),
        total_prompt_tokens=total_prompt,
        total_completion_tokens=total_completion,
    )


def load_adapter(module_path: str):
    """Dynamically load a model adapter from a module path."""
    mod = importlib.import_module(module_path)

    if hasattr(mod, "Adapter"):
        return mod.Adapter()
    if hasattr(mod, "DummyAdapter"):
        return mod.DummyAdapter()

    raise ImportError(
        f"Module '{module_path}' must define an 'Adapter' or 'DummyAdapter' class"
    )


def build_report(
    results: list[InstanceResult],
    *,
    benchmark_path: str,
    model_name: str | None = None,
    adapter_name: str | None = None,
) -> EvalReport:
    """Build an EvalReport from results and metadata."""
    aggregate = compute_aggregate(results)

    metadata: dict = {
        "benchmark": benchmark_path,
        "evaluated_at": datetime.now(timezone.utc).isoformat() + "Z",
        "total_instances": len(results),
        "judge_model": JUDGE_MODEL,
    }
    if model_name:
        metadata["model"] = model_name
    if adapter_name:
        metadata["adapter"] = adapter_name

    return EvalReport(
        metadata=metadata,
        aggregate=aggregate,
        results=results,
    )


def print_report_summary(aggregate: AggregateMetrics) -> None:
    """Print a human-readable summary of evaluation metrics."""
    print(f"\n{'=' * 60}")
    print("  VulnBench Evaluation Report")
    print(f"{'=' * 60}")
    print(f"  Instances evaluated:   {aggregate.total_instances}")
    print(f"  Passed (score >= 0.5): {aggregate.total_passed}")
    print(f"  Pass rate:             {aggregate.pass_rate:.1%}")
    print(f"  Mean judge score:      {aggregate.mean_score:.3f}")
    print(f"  Mean gen time:         {aggregate.mean_generation_time_s:.1f}s")
    if aggregate.total_cost_usd > 0:
        print(f"  Model cost:            ${aggregate.total_cost_usd:.4f}")
        print(f"  Judge cost:            ${aggregate.total_judge_cost_usd:.4f}")
        print(
            f"  Total tokens:          {aggregate.total_prompt_tokens}p"
            f" + {aggregate.total_completion_tokens}c"
        )
    print()
    if aggregate.pass_rate_by_tier:
        print("  Pass rate by tier:")
        for tier, rate in sorted(aggregate.pass_rate_by_tier.items()):
            print(f"    {tier}: {rate:.1%}")
    if aggregate.pass_rate_by_ecosystem:
        print("  Pass rate by ecosystem:")
        for eco, rate in sorted(aggregate.pass_rate_by_ecosystem.items()):
            print(f"    {eco}: {rate:.1%}")
    print(f"{'=' * 60}")


def main():
    parser = argparse.ArgumentParser(description="VulnBench Evaluation Harness")
    parser.add_argument(
        "--benchmark",
        type=str,
        required=True,
        help="Path to benchmark JSON (vulnbench_full.json or vulnbench_mini.json)",
    )

    model_group = parser.add_mutually_exclusive_group(required=True)
    model_group.add_argument(
        "--model",
        type=str,
        help="LiteLLM model identifier (e.g., openrouter/openai/gpt-5.4)",
    )
    model_group.add_argument(
        "--adapter",
        type=str,
        help="Python module path to model adapter (e.g., benchmark.adapters.base)",
    )

    parser.add_argument(
        "--temperature",
        type=float,
        default=0.0,
        help="Sampling temperature for LiteLLM models (default: 0.0)",
    )
    parser.add_argument(
        "--max-tokens",
        type=int,
        default=4096,
        help="Max response tokens for LiteLLM models (default: 4096)",
    )
    parser.add_argument(
        "--judge-model",
        type=str,
        default=JUDGE_MODEL,
        help=f"LiteLLM model ID for the judge (default: {JUDGE_MODEL})",
    )
    parser.add_argument(
        "--output",
        type=str,
        default="results/eval_report.json",
        help="Output path for evaluation report",
    )
    parser.add_argument(
        "--limit",
        type=int,
        default=0,
        help="Max instances to evaluate (0=all)",
    )

    args = parser.parse_args()

    # Load benchmark
    logger.info("Loading benchmark from %s", args.benchmark)
    bench_data = json.loads(Path(args.benchmark).read_text())
    benchmark = BenchmarkDatabase(**bench_data)
    instances = benchmark.instances

    if args.limit > 0:
        instances = instances[: args.limit]

    logger.info("Evaluating %d instances (judge: %s)", len(instances), args.judge_model)

    # Load adapter
    model_name: str | None = None
    adapter_name: str | None = None

    if args.model:
        from benchmark.adapters.litellm_adapter import LiteLLMAdapter

        model_name = args.model
        logger.info("Using LiteLLM model: %s", args.model)
        adapter = LiteLLMAdapter(
            model=args.model,
            temperature=args.temperature,
            max_tokens=args.max_tokens,
        )
    else:
        adapter_name = args.adapter
        logger.info("Loading adapter from %s", args.adapter)
        adapter = load_adapter(args.adapter)

    # Evaluate
    results: list[InstanceResult] = []
    pbar = tqdm(instances, desc="Evaluating")
    for instance in pbar:
        result = evaluate_instance(instance, adapter, judge_model=args.judge_model)
        results.append(result)
        pbar.set_postfix(
            passed=sum(1 for r in results if r.passed),
            score=f"{sum(r.score for r in results) / len(results):.3f}",
        )

    # Build and write report
    report = build_report(
        results,
        benchmark_path=args.benchmark,
        model_name=model_name,
        adapter_name=adapter_name,
    )

    output_path = Path(args.output)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(json.dumps(report.model_dump(), indent=2))

    # Summary
    print_report_summary(report.aggregate)
    print(f"  Report written to: {output_path}")


if __name__ == "__main__":
    main()
